// ── Utility functions ────────────────────────────────────────

/**
 * Get all unique technologies across all labs
 */
function getAllTechnologies() {
  const techs = new Set();
  LABS.forEach(lab => lab.technology.forEach(t => techs.add(t)));
  return Array.from(techs).sort();
}

/**
 * Get all unique platforms
 */
function getAllPlatforms() {
  const platforms = new Set();
  LABS.forEach(lab => platforms.add(lab.platform));
  return Array.from(platforms).sort();
}

/**
 * Get labs for a specific week
 */
function getLabsByWeek(week) {
  return LABS.filter(lab => lab.week === week);
}

/**
 * Get labs by technology
 */
function getLabsByTechnology(tech) {
  return LABS.filter(lab => lab.technology.includes(tech));
}

/**
 * Get stats about the lab collection
 */
function getLabStats() {
  return {
    total: LABS.length,
    byDifficulty: {
      beginner:     LABS.filter(l => l.difficulty === 'beginner').length,
      intermediate: LABS.filter(l => l.difficulty === 'intermediate').length,
      advanced:     LABS.filter(l => l.difficulty === 'advanced').length,
    },
    freeForever:  LABS.filter(l => l.freeForever).length,
    noCreditCard: LABS.filter(l => !l.creditCardRequired).length,
    highRelevance: LABS.filter(l => l.interviewRelevance === 'high').length,
    totalHours: Math.round(LABS.reduce((acc, l) => acc + l.durationMin, 0) / 60),
  };
}

export { LABS, getAllTechnologies, getAllPlatforms, getLabsByWeek, getLabsByTechnology, getLabStats };
